        <p class="lead"><strong>
  How are the coins generated?</strong><br />
  When a coin is first created it starts off as a string. This string is made up of the DATE, CURRENCY SYMBOL, WALLET ADDRESS, and a TRANSACTION KEY. This string is then hashed and the output is saved as .coin file. The body of the .coin file is the data string that was used to create the hash. This data string is then signed by the users private key and saved with the .coin file.</p>
         <hr> 
        <p class="lead"><strong>
  How is a wallet created?</strong><br />
  A wallet is generated by taking a data string with the wallet creators chosen UID, PASS KEY and the CURRENCY_SYMBOL. A MD5 hash is then created from this string and the output is saved as a folder with a .wallet extension. When a wallet is created an elliptic-curve key pair is also generated that is unique to the wallet. The public key is written to the public ledger along with the wallet address. The Private key is only accessible to the wallet owner.</p>
