<p class="lead"><strong>Do tulipcoin have any value?</strong></p>
<p class="lead">The market will decide the coins value.</p>
<hr />
<p class="lead"><strong>Do I have to store my tulipcoin at www.tulipcoin?</strong></p>
<p class="lead">No. You are free to download your coins and store them locally, offline, or at any other wallet that accepts them. You can even download a copy of your wallet with all your coins in it as a zip file.</p>
<hr />
<p class="lead"><strong>What is the plain-English Public Ledger?</strong></p>
<p class="lead">The Public Ledger is an open accounting of all transactions involving tulipcoin. Each transaction is timestamped and added to the ledger in an ascending order. The transactions are then verified and signed with a private key to ensure their validity. The ledger is a plain English flat file and is available for anyone to download. </p>
<hr />
<p class="lead"><strong>How does a public ledger decentralize tulipcoin?</strong></p>
<p class="lead">In order to provide decentralization the ledgers can also be hosted by third parties. The ledgers are each assigned a UID and their own unique Key Pair. The ledgers are designed to sync with another in an ascending order from the earliest available timestamped ledger to the newest. Since each ledger operates independently and has its own key pair forging a transaction is nearly impossible. </p>
<p class="lead">Here is an example of how it works:</p>
<p class="lead"> wallet c9ca29fab55d732ba68cbf7346252fcf creates a coin. This transaction is first recorded on the local ledger. Once it has been recorded it is then broadcast to all other ledgers on the network starting with the oldest first. As it records the transaction it is also checking the coins validity and history. The Ledger API first checks to see if a transaction has been recorded on each ledger it discovers in the network. If the transaction is already recorded it then moves to the next available ledger. If it hasn't been recorded it then records the transaction and keeps repeating this process.<br>
</p>
