<p class="lead"><strong>What is a tulipcoin?</strong></p>
<p class="lead">A tulipcoin is a hybrid cryptocurrency that was created for people to use as they want.</p>
<hr />
<p class="lead"><strong>How many tulipcoin will there be?</strong></p>
<p class="lead">Only 999999999999 will be created.</p>
<hr />
<p class="lead"><strong>Is this Cryptocurrency anonymous?</strong></p>
<p class="lead">Yes. No personal info is required, tracked, or collected with any transaction.</p>
<hr />
<p class="lead"><strong>How do I get tulipcoin?</strong></p>
<p class="lead">tulipcoin are distributed using the drop method. To get the coins all you need is one of our anonymous wallets and wait for the coins to drop on the drop page. </p>
<hr />
<p class="lead"><strong>What is a wallet?</strong></p>
<p class="lead">A wallet is an electronic account where you store your coins and where people send you coins. When a wallet is created the first step in the process is creating a Key Pair. The Key Pair is made up of both a Public Key and a Private Key. A hash is created by taking multiple variables and hashing them multiple times to come up with a unique and verifiable address for you to use.</p>
<hr />
<p class="lead"><strong>Can I have more then one wallet?</strong></p>
<p class="lead">Yes you can have as many wallets as you like. This is actually encouraged to help maintain anonymity. With each new wallet you will also get a new Key Pair for that wallet.</p>
<hr />
<p class="lead"><strong>How do I create a wallet?</strong></p>
<p class="lead">To get your own wallet all you need to do is click the Get A Wallet link in the main nav bar and fill out the simple required info.</p>
